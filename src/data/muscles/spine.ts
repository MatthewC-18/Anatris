// src/data/muscles/spine.ts
//
// Spine muscle data - the 3D / ROM source of truth for the cervical, thoracic
// and lumbar regions. Analogue of muscles/shoulder.ts and muscles/elbow.ts.
//
// The spine carries 41 distinguishable muscles. Because that is far too many to
// teach in one undifferentiated list, the module is split into THREE regions
// that are first-class peers of "shoulder"/"elbow" in the store:
//   cervical  -> cervicalMuscles  (suboccipitals, deep neck, craniocervical,
//                                   and the cervical portions of erector spinae
//                                   and transversospinalis)
//   thoracic  -> thoracicMuscles  (thoracic erector spinae + transversospinalis,
//                                   rotatores)
//   lumbar    -> lumbarMuscles    (lumbar erector spinae + transversospinalis,
//                                   quadratus lumborum, psoas, iliacus, and the
//                                   anterolateral abdominal wall)
//
// +---------------------------------------------------------------------------+
// | CLINICAL CONTENT DISCLAIMER
// | origin / insertion / innervation / actions / clinicalNote are drafted from
// | standard anatomy as a STARTING POINT and MUST be verified against an
// | authoritative source (Kapandji / Oatis / Dufour) before shipping. All such
// | fields here are unverified draft copy (pageVerified: false equivalent).
// +---------------------------------------------------------------------------+
//
// meshBases STATUS: VERIFIED. Every base below was produced by running the real
// parseMeshName() logic over the actual Z-Anatomy mesh names extracted from the
// model, then flattened the way Three.js flattens them at load time
// (spaces -> "_", dots -> ""). Notable non-obvious bases (verified, not guessed):
//
//   - PSOAS MAJOR: the bare belly "Psoas major" flattens to "Psoas_major" and
//     parseMeshName eats the trailing "r" as a side marker -> base "Psoas_majo".
//     The lateralized bellies "Psoas major.l/.r" -> "Psoas_major". BOTH listed.
//   - ROTATORES: the belly "Rotatores" plus the group node "Rotatores.g" ->
//     "Rotatoresg". BOTH listed. The atlas does NOT separate rotatores by level.
//   - INTERTRANSVERSARII: modelled in the lumbar region only, split into dorsal
//     and ventral parts plus a group node -> three distinct bases.
//   - ERECTOR SPINAE is only a GROUP NODE in the model (no single belly); the
//     real meshes are iliocostalis / longissimus / spinalis, listed individually.
//
// NOT MODELLED in this Z-Anatomy build (verified absent, so intentionally
// omitted): semispinalis capitis (only colli + thoracis exist) and psoas minor.

